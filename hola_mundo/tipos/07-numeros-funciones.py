# python no tiene muchas funciones nativas como para trabajar con números,
# pero podemos importar un módulo (archivo externo)

import math

print(round(1.3))
print(round(1.7))
print(round(1.5))
print(abs(-77))  # devuelve el número absoluto
print(abs(55))

# trae el número entero más cercano hacia arriba
print(math.ceil(1.1))
# lo lleva al número entero más cercano hacia abajo
print(math.floor(1.99999))
# el parámetro es un número?
print(math.isnan(23))
# elevar a la potencia
print(math.pow(10, 3))
# raiz cuadrada del número pasado por parámetro
print(math.sqrt(10, 3))
