# expresiones: es una cadena de instrucciones que despues van a evaluar en una Ãºnica cosa. Por ejemplo:
# 1 + 2
# n1 + n2
# cuando comparamos expresiones, se evalua y nos devuelve un boolean(True o False)
print(1 > 2)  # >>> False
print(1 < 2)  # >>> True
print(1 <= 2)  # >>> True
print(1 >= 2)  # >>> False
print(2 >= 2)  # >>> True
print(2 <= 2)  # >>> True
print(2 == 2)  # >>> True
print(2 == 3)  # >>> False
print(2 == "2")  # >>> False
print(2 != "2")  # >>> True
print(2 != 2)  # >>> False
